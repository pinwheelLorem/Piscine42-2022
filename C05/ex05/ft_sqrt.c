int	ft_sqrt(int nb)
{
	long	n;
	long	step;

	n = nb;
	step = 1;
	if (n < 1)
		return (0);
	while (step * step <= n)
	{
		if (step * step == n)
			return (step);
		step++;
	}
	return (0);
}
