#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	q;

	q = 2;
	if (nb <= 1)
		return (0);
	while (q <= (nb / 2))
	{
		if (!(nb % q))
			return (0);
		q++;
	}
	return (1);
}
