int	ft_find_next_prime(int nb)
{
	unsigned int	n;

	if (nb < 3)
		return (2);
	if (!(nb % 2))
		nb++;
	n = 3;
	while (n * n <= (unsigned int)nb)
	{
		if (!(nb % n))
		{
			nb += 2;
			n = 1;
		}
		n += 2;
	}
	return (nb);
}
