int	ft_checkbase(char *str)
{
	int	i;
	int	j;

	i = 0;
	if (str[i] == '\0' || str[1] == '\0')
		return (0);
	while (str[i])
	{
		if ((str[i] <= 13 && str[i] >= 9) || str[i] <= 32
			|| str[i] == 127 || str[i] == '-' || str[i] == '+')
			return (-1);
		j = i + 1;
		while (str[j])
		{
			if (str[i] == str[j])
				return (-1);
			j++;
		}
		i++;
	}
	return (i);
}
