#include <unistd.h>

void	ft_print_comb(void)
{	
	char	t[5];

	*t = '0';
	t[1] = '1';
	t[2] = '2';
	t[3] = ',';
	t[4] = ' ';
	while (*t < '8')
	{
		if (*t < '7')
			write(1, t, 5);
		else
			write(1, t, 3);
		t[2]++;
		if (t[2] > '9')
		{
			t[1]++;
			if (t[1] > '8')
			{
				(*t)++;
				t[1] = *t + 1;
			}
			t[2] = t[1] +1;
		}
	}
}
